<template>
    <div class="container">
      <form v-on:submit="sumbitArticle()">
          <div class="form-group">
            <label for="judul">Judul:</label>
            <input type="text" class="form-control" id="judul" v-model="articles.judul">
          </div>
          <div class="form-group">
            <label for="isi">Isi:</label>
            <textarea class="form-control" id="isi" rows="5" v-model="articles.isi"></textarea>
          </div>
          <button class="btn btn-primary">Submit</button>
    </form>
    </div>
</template>

<script>

export default {
  data() {
    return {
      articles: {
        judul : '',
        isi : '',
      },
      errors: []
    }
  },

  // Fetches posts when the component is created.
  methods: {
    sumbitArticle() {
    axios.post('/articles', this.articles)
    .then(response => {
      // JSON responses are automatically parsed.
      console.log(response);
      this.$router.push({path: '/'});
      this.articles = response.data
    })
    .catch(e => {
      this.errors.push(e)
    })
  }
  }
}
</script>

